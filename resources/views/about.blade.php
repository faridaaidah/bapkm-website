										<h2 style="padding: 0; margin-bottom: 1em; color: black;">Visi</h2>
									</header>
									<p align="justify">
										Evaluasi merupakan upaya menghimpun dan mengolah data yang terpercaya dan dapat dipergunakan sebagai landasan tindakan manajemen untuk mengelola kelangsungan institusi.
										Dari beberapa tipe evaluasi yang dilaksanakan, evaluasi diri merupakan tipe yang banyak diadopsi dan direkomendasikan untuk dilaksanakan dalam mengevaluasi hasil kerja institusi.
										Pada dasarnya, evaluasi diri merupakan proses pengukuran secara kuantitatif yang membandingkan prestasi yang telah dicapai dengan tujuan yang diinginkan.
										<br><br>
										<strong>Biro Administrasi Pembelajaran dan Kesejahteraan Mahasiswa (BAPKM)</strong>, sebagai unit yang berkewajiban memberikan pelayanan kepada <i>stakeholder</i> secara profesional, menetapkan standar pelayanan administrasi pembelajaran dan kesejahteraan mahasiswa yang mengacu pada prinsip relevansi dan efisiensi.
										Oleh karena itu, semangat kreatif dan inovatif juga terus dikembangkan oleh seluruh karyawan BAPKM.
										<br><br>
										Dalam upaya optimalisasi kinerja administrasi pembelajaran dan kesejahteraan mahasiswa, pada acara Raker 2017 diusulkan beberapa program hasil kreativitas dan inovasi yang merupakan sinergi antara pimpinan dan staf dalam mewujudkan pelayanan yang berkualitas, antara lain sebagai berikut:
										<ul>
											<li>Peningkatan fungsi Website BAPKM dengan alamat: <a href="/">www.baak.its.ac.id</a>, untuk informasi data mahasiswa seperti jumlah peminat, penerimaan dan pendaftaran ulang mahasiswa baru ITS dari semua jalur, dan
												SK Lulusan dan SK Mahasiswa yang terkena evaluasi studi. Ke depannya, <i>domain</i> web akan disesuaikan dengan nomenklatur yakni BAPKM.</li>
											<li>Pencetakan KRSM secara <i>online</i> dan mandiri oleh mahasiswa melalui Integra.</li>
											<li>Pencetakan Daftar Hadir Kuliah secara <i>online</i> dan mandiri oleh masing-masing departemen melalui integra.</li>
											<li>Pemantauan KHS dapat dilakukan secara <i>online</i> oleh orang tua mahasiswa melalui aplikasi <i>smartphone</i> Android tanpa harus lagi menunggu kiriman surat dari ITS.</li>
											<li>Pencetakan <i>draft</i> ijazah wisudawan dapat dilakukan secara mandiri melalui Integra untuk dikoreksi secara mandiri sebelum dikirim ke departemen. Sebelumnya perlu proses lama karena menunggu kehadiran calon wisudawan untuk mengoreksi.</li>
											<li>Pemindaian ijazah scara <i>soft copy (scan)</i> terus dikembangkan sebagai database alumni untuk pelayanan alumni.</li>
											<li>Pembuatan <i>virtual account</i> untuk pembayaran jasa layanan lain di BAPKM secara transfer. Jasa layanan lain meliputi <i>translate</i>, surat keterangan pengganti ijazah/transkip yang hilang. Layanan pun bisa dilakukan via <i>e-mail</i>.</li>
											<li>Penambahan <i>filter</i> di sistem siyudi untuk verifikasi judul Tugas Akhir (TA) dalam transkip nilai. Hal ini dilakukan untuk meminimalisir kesalahan judul TA dalam transkip nilai.</li>
											<li>Penerapan Program 5R (Rapi, Ringkas, Resik, Rawat, Rajin) pada penataan dokumen dan warehouse untuk mempermudah pelayanan dan tertib administrasi.</li>
											<li>Melakukan peningkatan mutu organisasi dan manajemen melalui implementasi  ISO 9001:2008 menuju ISO 9001:2015.</li>
											<li>Menerapakan Keselamatan dan Kesehatan Kerja (K3) di lingkungan BAPKM yang diawali dengan disahkanya peresmian kebijakan K3 oleh Kepala BAPKM.</li>
											<li>Menyusun dan memperbarui kembali SOP, baik pada bagian adminitrasi pembelajaran maupun kesejahteraan mahasiswa. Dibutuhkan kajian lanjutan untuk SOP terkait kerjasama dan tata kelola keuangan.</li>
											<li>Pengelolaan sistem peminjaman SCC untuk kegiatan mahasiswa.</li>
											<li>Penataan engelolaan dana beasiswa dan membangun hubungan kerjasama bagi mitra.</li>
										</ul>
										Program pengembangan tersebut diatas diusulkan agar daya dukung Biro Administrasi Pembelajaran dan Kesejahteraan Mahasiswa (BAPKM) sebagai unit pelayanan administrasi akademik bisa berfungsi dengan baik, yaitu terciptanya kerapian manajemen, <i>excellent service</i>,
										tercapainya tingkat relevansi yang tinggi, serta sebagai sentral data akademik yang akurat, dinamis, dan terpercaya, sehingga dapat digunakan oleh <i>stakeholder</i> dalam proses pengambilan keputusan.
										<br><br>
										Sesuai dengan paradigma baru pengembangan pendidikan tinggi yang berorientasi pada prinsip kemandirian, maka Visi BAPKM adalah:
										<br><strong>“Menjadikan BAPKM sebagai unit penunjang utama dengan mengedepankan layanan administrasi yang <i>excellent</i>”.</strong>
									</p>
								</section>

